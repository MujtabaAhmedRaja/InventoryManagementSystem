class Product {
    public int id;
    public String name;
    public int quantity;
    public double price;
    public Supplier supplier;




    public Product(int id, String name, int quantity, double price, Supplier supplier) {
        this.id = id;
        this.name = name;
        this.quantity = quantity;
        this.price = price;
        this.supplier = supplier;


    }
}
